// Generates the build-time CMYK→sRGB lookup table used by convert/color/ColorManager.ts.
//
// This is a ONE-OFF content-generation step (like `inject-idml-plain`), not part of
// `npm run build` — its OUTPUT (src/assets/CMYK_LUT.ts) is committed to the repo and is
// what actually ships. `lcms-wasm` (LittleCMS in WASM) and the source ICC profile are
// build-time-only inputs: the published package has NO WASM and NO .icc file, only the
// derived numeric table + pure-JS interpolation (see ColorManager.ts).
//
// Re-run this script only if the bundled ICC profile changes or the grid resolution needs
// tuning:  npm run generate-cmyk-lut
//
// Validated against InDesign's actual rendering (feat-cmyk ground truth): pure C/M/Y
// primaries transform to an EXACT pixel match (0,174,239) / (236,0,140) / (255,242,0)
// with Relative Colorimetric + Black Point Compensation — hence that intent/flag choice.
import { readFile, writeFile } from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';
import { instantiate } from 'lcms-wasm';
import { TYPE_CMYK_8, TYPE_RGB_8, INTENT_RELATIVE_COLORIMETRIC, cmsFLAGS_BLACKPOINTCOMPENSATION } from 'lcms-wasm/lib/constants.js';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const ICC_PATH = path.join(root, 'src/assets/icc/USWebCoatedSWOP.icc');
const OUT_PATH = path.join(root, 'src/assets/CMYK_LUT.ts');

// 17 steps/channel (6.25% spacing) — smooth enough for quadrilinear interpolation to be
// visually exact between grid points (SWOP's A2B tables have no sharp discontinuities).
// 17^4 = 83521 grid points, 3 bytes (sRGB) each ≈ 245KB raw / ~330KB base64 in the committed file.
const N = 17;

async function main() {
  const lcms = await instantiate();
  const iccBytes = await readFile(ICC_PATH);
  const hIn = lcms.cmsOpenProfileFromMem(new Uint8Array(iccBytes.buffer, iccBytes.byteOffset, iccBytes.byteLength), iccBytes.byteLength);
  if (!hIn) throw new Error(`could not open ICC profile at ${ICC_PATH}`);
  const hOut = lcms.cmsCreate_sRGBProfile();

  // TYPE_CMYK_8/TYPE_RGB_8 (integer formats), NOT the *_DBL float formats — this WASM
  // build's JS wrapper miscomputes the byte width for the DBL formatters (allocates
  // 4-byte-per-sample buffers for what LittleCMS treats as 8-byte doubles internally),
  // producing garbage/uninitialized-heap output. Verified via direct comparison against
  // InDesign's rendered primaries: TYPE_CMYK_8/TYPE_RGB_8 gives an EXACT match; TYPE_*_DBL
  // does not (confirmed by hand before writing this script).
  const transform = lcms.cmsCreateTransform(hIn, TYPE_CMYK_8, hOut, TYPE_RGB_8, INTENT_RELATIVE_COLORIMETRIC, cmsFLAGS_BLACKPOINTCOMPENSATION);
  if (!transform) throw new Error('cmsCreateTransform failed');

  // Grid iteration order MUST match ColorManager.ts's index(c,m,y,k) formula:
  //   index = ((c*N + m)*N + y)*N + k
  const step = (i) => Math.round((i / (N - 1)) * 255);
  const input = new Uint8Array(N * N * N * N * 4);
  let p = 0;
  for (let ci = 0; ci < N; ci++) {
    for (let mi = 0; mi < N; mi++) {
      for (let yi = 0; yi < N; yi++) {
        for (let ki = 0; ki < N; ki++) {
          input[p++] = step(ci);
          input[p++] = step(mi);
          input[p++] = step(yi);
          input[p++] = step(ki);
        }
      }
    }
  }

  const output = lcms.cmsDoTransform(transform, input, N * N * N * N);
  lcms.cmsDeleteTransform(transform);

  const rgb = new Uint8Array(N * N * N * N * 3);
  for (let i = 0; i < N * N * N * N; i++) {
    rgb[i * 3] = output[i * 3];
    rgb[i * 3 + 1] = output[i * 3 + 1];
    rgb[i * 3 + 2] = output[i * 3 + 2];
  }

  const base64 = Buffer.from(rgb.buffer, rgb.byteOffset, rgb.byteLength).toString('base64');
  const ts = `// GENERATED by scripts/generate-cmyk-lut.mjs — do not hand-edit.
// CMYK -> sRGB lookup table, derived from src/assets/icc/USWebCoatedSWOP.icc
// ("U.S. Web Coated (SWOP) v2", InDesign's default CMYK working profile) via LittleCMS
// (lcms-wasm), Relative Colorimetric intent + Black Point Compensation.
// Grid: ${N} steps/channel (0..255 in equal steps), row-major C,M,Y,K.
// Consumed by convert/color/ColorManager.ts (quadrilinear interpolation at runtime).
export const CMYK_LUT_GRID_SIZE = ${N};
export const CMYK_LUT_BASE64 = '${base64}';
`;
  await writeFile(OUT_PATH, ts, 'utf8');
  console.log(`wrote ${OUT_PATH} (grid ${N}^4=${N ** 4} points, ${rgb.byteLength} raw bytes, ${base64.length} base64 chars)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
